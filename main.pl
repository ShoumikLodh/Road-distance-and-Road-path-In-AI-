dfs(Distance,Start,End,_,Visited):-
    %reaching goal state
    goal(Start),
    reverse(Visited, Path),
    nl,write("The distance is "),write(Distance),nl,
   show_path(Path),write(End).


dfs(Distance,Start,End,Unvisited,Visited):-
   %select first elem of unvisited and put it in front of tail
    [_|Tail]=Unvisited,
    distance(Start,Node,Dis),
    %such that next node is not closed
   \+(member(Node,Visited)),
    Total is Dis+Distance,
    %prevent infinite calls
    not(Node==Start),

    dfs(Total,Node,End,[Node|Tail],[Start|Visited]).


bfs(Distance1, Start1, End1,Unvisited1, Visited1 ):-
    [Head|Tail] = Unvisited1,
   Var1-Startnew = Head,


   %head is now a key value pair where X=Distance and Startnew is location
    %findall distance-location pairs, where the distance and location exist in the heuristics and the location is still unvisited. These pairs are stored in Node1 list
    findall(ShortDis-Location,(distance(Startnew,Location,_),
    not(Startnew == Location),not(member(Location,Visited1)),
    heur(Location,End1,ShortDis)),Node1),

    append(Node1,Tail, NewUnvisited ),
    % appends the tail to Node1 and then storing it in to the updated Visited list.
    % sorting the updated visited list according to its key distances
    keysort(NewUnvisited,SortedNodes),

    [Shortest|_]= SortedNodes,
    % except for closesT node rest closest stored in the sortednodes

    Var2-NeighbourLocation=Shortest,
    %closest node key value pair in shortest
   % fn(X,Y),
    distance(Start1, NeighbourLocation, Dist),
    Total1 is Distance1+Dist,
    bfs(Total1, NeighbourLocation, End1, SortedNodes, [Start1|Visited1]).


